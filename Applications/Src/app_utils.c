uint16_t u16UTILS_GetCheckCrc(uint8_t *puchMsg, uint16_t usDataLen) //ModbusRTU
{ 
  uint8_t uchCRCHi = 0xFF;
  uint8_t uchCRCLo = 0xFF;
  uint16_t uIndex;
  while (usDataLen--)	{
   
          uIndex = uchCRCLo ^ ((*puchMsg)&0xFF); puchMsg++;
          uchCRCLo = uchCRCHi ^ auchCRCHi[uIndex]; 
          uchCRCHi = auchCRCLo[uIndex]; 
  } 
  return (uchCRCHi << 8 | uchCRCLo); 
}

