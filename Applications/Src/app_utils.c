/******************************************************************************************
*                                     DPRINT
* Description : 打印模块,可根据信息级别进行打印输出
* Argument(s) : DPRINT_TYPE type   : 打印信息类型
*               const char* format: 打印信息内容,与printf使用方法一致
* Returns     : None
******************************************************************************************/
void vUTILS_LOG_PRINT(LOG_PRINT_TYPE type, const char* format, ...)
{
	va_list ap;
	char s[LOG_PRINT_MAXSTRLEN];
	char* infotype = NULL;

	if (type < print_level)		//打印信息等级低于设定级别,则不打印
		return;

	va_start(ap, format);
	vsprintf(s, format, ap);
	va_end(ap);

	LOG_PRINT_BG_COLOR    bg_color = bg_black;
	LOG_PRINT_FRONT_COLOR front_color = front_write;

	switch (type)
	{
	case __NULL__:  infotype = "";          break;
	case __DEBUG__: infotype = "[DEBUG]: "; break;
	case __INFO__:  infotype = "[INFO ]: "; break;
	case __WARN__:  infotype = "[WARN ]: "; front_color = front_yellow; break;
	case __ERROR__: infotype = "[ERROR]: "; front_color = front_red;	break;
	case __FATAL__: infotype = "[FATAL]: "; front_color = front_purple;	break;
	default:        infotype = "[USER ]: "; break;
	}

#if(LOG_PRINT_COLOR_SUPPORT==1)
	printf("\033[0;%d;%dm%s%s\033[0m", front_color, bg_color, infotype, s);
#else
	front_color = front_color;   //无意义,只用于消除编译器警告(unused)
	bg_color = bg_color;
	printf("%s%s", infotype, s);
#endif
}

void vUTILS_LOG_PRINT_LEVEL_SET(LOG_PRINT_TYPE level)
{
	print_level = level;
}
